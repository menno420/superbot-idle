CANONICAL_EMPTY = (
    '{"balances":{},"last_seen":0,"lifetime":{},"milestones":{},"owned":{},'
    '"prestige":{},"state_version":2,"upgrades":{}}'
)

CANONICAL_V1_EMPTY = (
    '{"balances":{},"last_seen":0,"lifetime":{},"owned":{},'
    '"prestige":{},"state_version":1,"upgrades":{}}'
)

DOCUMENTED_ERRORS = (
    MalformedSaveError,
    UnknownVersionError,
    FieldSetError,
    FieldTypeError,
    NegativeValueError,
)


def _valid_doc(**overrides) -> dict:
    """A well-formed v2 document, tweakable per test."""
    doc = {
        "state_version": 2,
        "balances": {"cur-a": 42},
        "owned": {"gen-a": 3},
        "last_seen": 1_700_000_000,
        "upgrades": {"up-a": 2},
        "lifetime": {"cur-a": 999},
        "prestige": {"meta": 1},
        "milestones": {"owned-1": 1},
    }
    doc.update(overrides)
    return doc


def _valid_v1_doc(**overrides) -> dict:
    """A well-formed LEGACY v1 document (no milestones field)."""
    doc = _valid_doc(state_version=1)
    del doc["milestones"]
    doc.update(overrides)
    return doc


# --- 1. canonical determinism ---------------------------------------------------


def test_empty_state_canonical_literal_is_pinned():
    assert dump_state(GameState()) == CANONICAL_EMPTY


def test_dump_is_deterministic_and_insertion_order_free():
    a = GameState(balances={"x": 1, "y": 2}, owned={"g": 1}, lifetime={"y": 4, "x": 3})
    b = GameState(balances={"y": 2, "x": 1}, owned={"g": 1}, lifetime={"x": 3, "y": 4})
    assert a == b  # dict equality ignores order...
    assert dump_state(a) == dump_state(b) == dump_state(a)  # ...and so does the dump


def test_dump_has_no_whitespace_floats_or_non_ascii():
    state = GameState(balances={"café": 7}, last_seen=5)
    text = dump_state(state)
    assert " " not in text and "\n" not in text
    assert "." not in text  # ints only, so no float ever prints a dot
    assert text.isascii()
    assert load_state(text) == state  # escapes round-trip


@pytest.mark.parametrize("seed", range(20))
def test_same_seed_same_bytes(seed):
    state = _random_roster(Random(11_000 + seed))[0]
    again = _random_roster(Random(11_000 + seed))[0]
    assert dump_state(state) == dump_state(again)


def test_doc_worked_example_dumps_exactly():
    """md-parity: the doc's canonical example is generated by the real encoder."""
    text = DOC_PATH.read_text(encoding="utf-8")
    blocks = re.findall(r"```json\n(.*?)\n```", text, re.DOTALL)
    assert blocks, "docs/persistence.md must carry a ```json worked example"
    empty = dump_state(GameState())
    example = dump_state(
        GameState(
            balances={"cur-a": 42},
            owned={"gen-a": 3},
            last_seen=1_700_000_000,
            upgrades={"up-a": 2},
            lifetime={"cur-a": 999},
            prestige={"meta": 1},
            milestones={"owned-1": 1},
        )
    )
    # Each documented block is byte-identical to a real dump (strip the
    # doc's display newlines: the examples are single-line literals).
    literals = [block.strip() for block in blocks]
    assert empty in literals and example in literals, (
        "doc examples drifted from the real encoder", literals, [empty, example]
    )


